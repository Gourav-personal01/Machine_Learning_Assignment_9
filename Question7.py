# Q7. You are a data analyst for a flight booking website, and you have been asked to analyze the Flight
# Price dataset to identify any trends in flight prices. What features would you analyze to identify these
# trends, and what visualizations would you use to present your findings to your team?

# Features to Analyze:

# Date or Time: Analyze the temporal aspect by considering date, month, day of the week, and time of day.

# Route or Destination: Examine how flight prices vary based on different routes or destinations.

# Airlines: Investigate pricing trends among different airlines.

# Advance Booking: Analyze how the lead time (number of days between booking and departure) affects flight prices.

# Seasonality: Identify seasonal trends in flight prices, such as holidays, summer vacations, or specific events.

# Flight Duration: Check if there are pricing differences between short-haul and long-haul flights.

# Visualization to use - 
# box plot
# scatter plot
# heatmap etc.